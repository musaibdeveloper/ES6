// Fat Arrow function is alternate way to write a function.
// Normal function

function hello(){
    console.log("Hey :)");
}
hello(); // Hey :)

// The above is normal function.

// Fat arrow function for the above code.

const Hello1 = () =>{
    console.log("hey :)");
}

Hello1(); // hey :)

