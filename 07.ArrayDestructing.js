// Array destructuring.

let  nums = ['one', 'two', 'three'];

// destructuring assignment in arrays
let [x, y, z] = nums;

console.log(x); // one
console.log(y); // two
console.log(z); // three

// Swapping the Array.

let a = 10;
let b = 25;

[a,b] = [b,a]

console.log(a); // 25

console.log(b); // 10